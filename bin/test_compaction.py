#!/usr/bin/env python3
"""Acceptance test for the compaction pass.

Builds a fully synthetic in-memory playbook with fictional near-duplicate pairs
and verifies that the compaction model proposes all three supersessions, including
the mandatory cross-section pair (DR section entry superseded by AG section entry).

No real project data is read. All entry IDs, text, and SHAs are illustrative.

Ground-truth pairs (fictional):
  GR-0023 / GR-0027  — same section (General Rules): config-reload atomicity
  DR-0005 / AG-0012  — CROSS-SECTION (Domain Rules → Architecture Gotchas): API timeout
  AG-0041 / AG-0045  — same section (Architecture Gotchas): cache-invalidation timing

Usage:
    python bin/test_compaction.py
    python bin/test_compaction.py --model sonnet
"""

from __future__ import annotations

import argparse
import json
import os
import re
import sys

import dream_lib as dl
from compaction import COMPACTION_SCHEMA, _build_compaction_prompt

DEFAULT_MODEL = os.environ.get("DREAM_MODEL", "haiku")

# Fictional near-duplicate pairs used as ground truth.
# At least one must be cross-section (the DR-0005 / AG-0012 pair).
GROUND_TRUTH_PAIRS: list[tuple[str, str]] = [
    ("GR-0023", "GR-0027"),   # same section: config-reload atomicity
    ("DR-0005", "AG-0012"),   # CROSS-SECTION: API-call timeout (DR → AG)
    ("AG-0041", "AG-0045"),   # same section: cache-invalidation timing
]


def _make_synthetic_playbook() -> str:
    """Build a fully in-memory synthetic playbook with fictional near-duplicate pairs.

    No files are read. All entries are crafted inline with illustrative content.
    """
    preamble = (
        "# Project Dream Playbook (synthetic test fixture)\n\n"
        "Do not hand-edit. This playbook is generated by test_compaction.py."
    )
    sections = [
        dl.Section(title=t, prefix=p) for t, p in dl.SECTIONS
    ]
    pb = dl.Playbook(preamble=preamble, sections=sections)

    def _e(
        eid: str, section: str, text: str, sha: str = "synth01"
    ) -> dl.Entry:
        source = "demo-feat-" + eid.split("-")[0].lower()
        return dl.Entry(
            id=eid,
            source=source,
            sha=sha,
            status="active",
            section=section,
            text=text,
        )

    # General Rules pair — same section
    gr = pb.section("General Rules")
    gr.entries.extend([
        _e(
            "GR-0023", "General Rules",
            "- **GR-0023** - Configuration reloads should be atomic; "
            "partial reloads leave the system in an inconsistent transient state. "
            "_(demo-feat-gr @synth01)_",
        ),
        _e(
            "GR-0027", "General Rules",
            "- **GR-0027** - Configuration reloads must be atomic: write the new "
            "config to a temp file and replace atomically; partial reloads cause "
            "transient inconsistency that is hard to reproduce under load. "
            "_(demo-feat-gr @synth02)_",
            sha="synth02",
        ),
    ])

    # Cross-section pair: DR-0005 (Domain Rules) and AG-0012 (Architecture Gotchas)
    dr = pb.section("Domain Rules")
    dr.entries.append(
        _e(
            "DR-0005", "Domain Rules",
            "- **DR-0005** - External API calls must always set a timeout; "
            "hanging calls block the worker pool. "
            "_(demo-feat-dr @synth01)_",
        )
    )

    ag = pb.section("Architecture Gotchas")
    ag.entries.extend([
        _e(
            "AG-0012", "Architecture Gotchas",
            "- **AG-0012** - Blocking external API calls without a timeout starve "
            "the worker thread pool; always set a connect+read timeout pair and wrap "
            "in a circuit-breaker to bound cascade failures. "
            "_(demo-feat-ag @synth02)_",
            sha="synth02",
        ),
        # Architecture Gotchas pair — same section
        _e(
            "AG-0041", "Architecture Gotchas",
            "- **AG-0041** - Cache invalidation must happen before the write completes. "
            "_(demo-feat-ag @synth03)_",
            sha="synth03",
        ),
        _e(
            "AG-0045", "Architecture Gotchas",
            "- **AG-0045** - Cache invalidation must occur before the database write "
            "commits; invalidating after creates a race window where stale data is "
            "served to concurrent readers that arrive between the write and the "
            "invalidation. _(demo-feat-ag @synth04)_",
            sha="synth04",
        ),
    ])

    return dl.render_playbook(pb)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(
        description="Verify compaction catches all fictional ground-truth pairs."
    )
    ap.add_argument("--model", default=DEFAULT_MODEL)
    ap.add_argument("--verbose", action="store_true")
    args = ap.parse_args(argv)

    print("building synthetic playbook with fictional near-duplicate pairs ...")
    synthetic_text = _make_synthetic_playbook()

    # Verify the synthetic playbook parses correctly and all pairs are active
    pb_check = dl.parse_playbook(synthetic_text)
    by_id = {e.id: e for e in pb_check.all_entries()}
    for sup_id, can_id in GROUND_TRUTH_PAIRS:
        sup_e = by_id.get(sup_id)
        can_e = by_id.get(can_id)
        if sup_e is None:
            print(f"SETUP ERROR: {sup_id} not in synthetic playbook", file=sys.stderr)
            return 2
        if can_e is None:
            print(f"SETUP ERROR: {can_id} not in synthetic playbook", file=sys.stderr)
            return 2
        assert sup_e.status == "active", f"{sup_id} should be active"
        assert can_e.status == "active", f"{can_id} should be active"

    # Verify the cross-section pair actually spans two different sections
    cross_sup, cross_can = "DR-0005", "AG-0012"
    assert by_id[cross_sup].section != by_id[cross_can].section, (
        f"cross-section pair {cross_sup}/{cross_can} must be in different sections"
    )
    print(
        f"  cross-section pair confirmed: {cross_sup} ({by_id[cross_sup].section}) "
        f"vs {cross_can} ({by_id[cross_can].section})"
    )

    print(f"running compaction scan with model={args.model} ...")
    prompt = _build_compaction_prompt(synthetic_text)
    system_prompt = dl.read_text(dl.prompts_dir() / "compaction.system.md")
    data, cost = dl.run_claude_json(
        system_prompt=system_prompt,
        user_prompt=prompt,
        schema_json=json.dumps(COMPACTION_SCHEMA),
        model=args.model,
    )
    print(f"  cost: ${cost:.4f}")

    proposed = {
        c["superseded_id"]: c["canonical_id"]
        for c in data.get("compactions", [])
    }
    if args.verbose:
        for sup, can in proposed.items():
            print(f"  proposed: {sup} -> {can}")

    failures: list[str] = []
    for sup_id, can_id in GROUND_TRUTH_PAIRS:
        if sup_id not in proposed:
            failures.append(f"MISS: {sup_id} not proposed as superseded")
        elif proposed[sup_id] != can_id:
            failures.append(
                f"WRONG_CANONICAL: {sup_id} -> {proposed[sup_id]} (expected {can_id})"
            )
        else:
            cross = by_id[sup_id].section != by_id[can_id].section
            label = "  (cross-section!)" if cross else ""
            print(f"  PASS: {sup_id} -> {can_id}{label}")

    # Report extra proposed supersessions (false positives are a review risk)
    expected_sups = {p[0] for p in GROUND_TRUTH_PAIRS}
    extras = [s for s in proposed if s not in expected_sups]
    if extras:
        print(f"  NOTE: {len(extras)} extra proposal(s) (review carefully): {extras}")

    if failures:
        print(f"\nFAIL: {len(failures)} ground-truth pair(s) not caught:")
        for f in failures:
            print(f"  - {f}")
        return 1

    # Explicitly assert the cross-section pair was caught
    cross_sup_id, cross_can_id = "DR-0005", "AG-0012"
    if proposed.get(cross_sup_id) != cross_can_id:
        print(
            f"\nFAIL: cross-section pair {cross_sup_id}/{cross_can_id} not caught",
            file=sys.stderr,
        )
        return 1

    n = len(GROUND_TRUTH_PAIRS)
    print(
        f"\nPASS: all {n} ground-truth pairs caught "
        f"(including cross-section {cross_sup_id}/{cross_can_id})"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
